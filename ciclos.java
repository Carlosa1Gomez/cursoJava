/**
 * ciclos
 */
public class ciclos {

  public static void main(String[] args){

    int numero = 10, i = 0;

    while (i <= numero) {

      System.out.println(i);

      i++;

    }

    do {

      System.out.println(i);

      i++;

    } while (i <= numero);

    for (int j = 0; j <= numero; j++) {

      System.out.println(i);

    }
  }
}
